	// {
	// 	UE_LOG(LogTemp, Warning, TEXT("Health from GetHealth: %f"), GetHealth());
	// 	UE_LOG(LogTemp, Warning, TEXT("Magnitude: %f"), Data.EvaluatedData.Magnitude); // 얼마나 바뀌는지
